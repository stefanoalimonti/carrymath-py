class TestCovarianceVectors:
    def test_bulk_covariance_j3(self):
        cv = VECTORS["covariance"]
        r = bulk_covariance(cv["K"])
        from fractions import Fraction

        assert r["cov"][3] == Fraction(cv["cov_j3"][0], cv["cov_j3"][1])

    def test_lemma_d(self):
        cv = VECTORS["covariance"]
        r = verify_lemma_d(3)
        assert r["lemma_d_holds"] == cv["lemma_d_K3"]

    def test_induction_step(self):
        cv = VECTORS["covariance"]
        from fractions import Fraction

        r = induction_step(cv["K"])
        assert r[1] == Fraction(cv["induction_step_j1"][0], cv["induction_step_j1"][1])


