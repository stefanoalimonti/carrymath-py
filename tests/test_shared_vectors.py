"""Tests driven by the shared test_vectors.json file.

This is the cross-language validation source of truth,
generated by Python and consumed by JS, PY, and C.
"""

import json
from pathlib import Path

import pytest

from carrymath import (
    bfs_simple,
    bulk_covariance,
    carry_chain,
    carry_polynomial,
    carry_polynomial_coeffs,
    convolution,
    discrete_log,
    divisor_component,
    euler_totient,
    from_digits,
    induction_step,
    legendre_symbol,
    modular_matrix_row,
    multiplicative_order,
    phantom_counts,
    poly_roots_mod,
    primitive_root,
    to_digits,
    verify_lemma_d,
)

_VECTORS_PATH = Path(__file__).resolve().parent / "data" / "test_vectors.json"
with open(_VECTORS_PATH) as f:
    VECTORS = json.load(f)


class TestCarryVectors:
    @pytest.fixture(params=VECTORS["carry_vectors"], ids=lambda v: f"{v['p']}*{v['q']}")
    def v(self, request):
        return request.param

    def test_digits_p(self, v):
        assert to_digits(v["p"], v["base"]) == v["digits_p"]

    def test_digits_q(self, v):
        assert to_digits(v["q"], v["base"]) == v["digits_q"]

    def test_digits_N(self, v):
        assert to_digits(v["N"], v["base"]) == v["digits_N"]

    def test_from_digits_roundtrip(self, v):
        assert from_digits(v["digits_p"], v["base"]) == v["p"]
        assert from_digits(v["digits_q"], v["base"]) == v["q"]

    def test_carry_chain(self, v):
        cc = carry_chain(v["digits_p"], v["digits_q"], v["base"])
        assert cc == v["carry_chain"]

    def test_convolution(self, v):
        if not v["convolution"]:
            pytest.skip("empty convolution vector")
        conv = convolution(v["digits_p"], v["digits_q"])
        assert conv == v["convolution"]

    def test_carry_polynomial(self, v):
        cp = carry_polynomial(v["p"], v["q"], v["base"])
        assert cp == v["carry_polynomial"]

    def test_carry_polynomial_coeffs(self, v):
        cc = carry_polynomial_coeffs(v["p"], v["q"], v["base"])
        assert cc == v["carry_polynomial_coeffs"]


